#include "gpio.h"

void
gpio_init (void)
{
	LATA = 0b00000000;
	LATB = 0b00000000;
	LATC = 0b00100001;
	LATD = 0b00000000;
	LATE = 0b00000011;
	LATF = 0b00000000;
	LATG = 0b00010000;

	TRISA = 0b11000000;
	TRISB = 0b11001111;
	TRISC = 0b11011000;
	TRISD = 0b11111111;
	TRISE = 0b11111100;
	TRISF = 0b00011111;
	TRISG = 0b11101111;
}
